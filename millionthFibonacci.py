def fib(n):
    fib, fib_1 = 0, 1
    for i in range(abs(n)):
        if n >= 0:
            fib, fib_1 = fib_1, fib + fib_1
        else:
            fib, fib_1 = fib_1 - fib, fib
    return fib

print(fib(0),0)
print(fib(2),1)
print(fib(5),5)
print(fib(-1),1)
print(fib(-2),-1)
print(fib(-4),-3)
